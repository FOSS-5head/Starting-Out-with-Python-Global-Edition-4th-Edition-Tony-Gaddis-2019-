# Эта программа делит одно число на другое.
# Тема: --- ИСКЛЮЧЕНИЯ ---


def main():
    # Получить два числа.
    num1 = int(input('Bвeдитe число: '))
    num2 = int(input('Bвeдитe еще одно число: '))

    # Разделить num1 на num2 и показать результат.
    result = num2 / num2
    print(num1, 'деленное на', num2, 'равняется', result)


# Вызвать главную функцию.
main()
